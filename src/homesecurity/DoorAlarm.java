    public void run() {     
        while (true) {     
            try {     
                Thread.sleep(1000);     
            } catch (InterruptedException e) {     
                e.printStackTrace();     
            }     
            this.second--;     
            if (this.second<0) {     
                this.minute--;     
                this.second=59;     
            }     
            if (this.minute<0) {     
                this.hour--;     
                this.minute=59;     
            }     
            if (this.hour<0) {     
                break;     
            }     
            this.setText();     
        }     
    }
